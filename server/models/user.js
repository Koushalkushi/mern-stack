const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  //the feilds should be mentioned below
  {
    name: {
      type: String,
      trim: true,
      required: true,
    },
    email: {
      type: String,
      trim: true,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("User", UserSchema); // i will call it user and call it UserSchema
